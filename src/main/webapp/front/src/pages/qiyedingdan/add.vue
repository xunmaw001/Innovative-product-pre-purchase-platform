<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            chanpinmingcheng : false,
            xiangmufenlei : false,
            qiyemingcheng : false,
            fahuoriqi : false,
            peisongjindu : false,
            peisongbeizhu : false,
            touzizhanghao : false,
            lianxidianhua : false,
            shouhuodizhi : false,
            crossuserid : false,
            crossrefid : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          chanpinmingcheng: '',
          xiangmufenlei: '',
          qiyemingcheng: '',
          fahuoriqi: '',
          peisongjindu: '',
          peisongbeizhu: '',
          touzizhanghao: '',
          lianxidianhua: '',
          shouhuodizhi: '',
          crossuserid: '',
          crossrefid: '',
        },
        peisongjinduOptions: [],
        rules: {
          chanpinmingcheng: [
          ],
          xiangmufenlei: [
          ],
          qiyemingcheng: [
          ],
          fahuoriqi: [
          ],
          peisongjindu: [
            { required: true, message: '配送进度不能为空', trigger: 'blur' },
          ],
          peisongbeizhu: [
          ],
          touzizhanghao: [
          ],
          lianxidianhua: [
          ],
          shouhuodizhi: [
          ],
          crossuserid: [
          ],
          crossrefid: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.fahuoriqi = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='chanpinmingcheng'){
              this.ruleForm.chanpinmingcheng = obj[o];
              this.ro.chanpinmingcheng = true;
              continue;
            }
            if(o=='xiangmufenlei'){
              this.ruleForm.xiangmufenlei = obj[o];
              this.ro.xiangmufenlei = true;
              continue;
            }
            if(o=='qiyemingcheng'){
              this.ruleForm.qiyemingcheng = obj[o];
              this.ro.qiyemingcheng = true;
              continue;
            }
            if(o=='fahuoriqi'){
              this.ruleForm.fahuoriqi = obj[o];
              this.ro.fahuoriqi = true;
              continue;
            }
            if(o=='peisongjindu'){
              this.ruleForm.peisongjindu = obj[o];
              this.ro.peisongjindu = true;
              continue;
            }
            if(o=='peisongbeizhu'){
              this.ruleForm.peisongbeizhu = obj[o];
              this.ro.peisongbeizhu = true;
              continue;
            }
            if(o=='touzizhanghao'){
              this.ruleForm.touzizhanghao = obj[o];
              this.ro.touzizhanghao = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
            if(o=='shouhuodizhi'){
              this.ruleForm.shouhuodizhi = obj[o];
              this.ro.shouhuodizhi = true;
              continue;
            }
            if(o=='crossuserid'){
              this.ruleForm.crossuserid = obj[o];
              this.ro.crossuserid = true;
              continue;
            }
            if(o=='crossrefid'){
              this.ruleForm.crossrefid = obj[o];
              this.ro.crossrefid = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.qiyemingcheng!=''&&json.qiyemingcheng) || json.qiyemingcheng==0){
                this.ruleForm.qiyemingcheng = json.qiyemingcheng
            }
          }
        });
        this.peisongjinduOptions = "进行中,已完成".split(',')
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('qiyedingdan/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('qiyedingdan/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('qiyedingdan/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('qiyedingdan/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
    }
  };
</script>

