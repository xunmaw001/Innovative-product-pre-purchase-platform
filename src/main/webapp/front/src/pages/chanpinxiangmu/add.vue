<script>
  export default {
    data() {
      return {
        id: '',
        baseUrl: '',
        ro:{
            chanpinmingcheng : false,
            xiangmufenlei : false,
            chanpintupian : false,
            chanpinguige : false,
            rongzijine : false,
            suochoujine : false,
            shengchanchandi : false,
            shangjiariqi : false,
            chanpinxiangqing : false,
            qiyemingcheng : false,
            lianxidianhua : false,
            sfsh : false,
            shhf : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          chanpinmingcheng: '',
          xiangmufenlei: '',
          chanpintupian: '',
          chanpinguige: '',
          rongzijine: '50000',
          suochoujine: '0',
          shengchanchandi: '',
          shangjiariqi: '',
          chanpinxiangqing: '',
          qiyemingcheng: '',
          lianxidianhua: '',
        },
        xiangmufenleiOptions: [],
        rules: {
          chanpinmingcheng: [
            { required: true, message: '产品名称不能为空', trigger: 'blur' },
          ],
          xiangmufenlei: [
          ],
          chanpintupian: [
          ],
          chanpinguige: [
          ],
          rongzijine: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          suochoujine: [
            { validator: this.$validate.isIntNumer, trigger: 'blur' },
          ],
          shengchanchandi: [
          ],
          shangjiariqi: [
          ],
          chanpinxiangqing: [
          ],
          qiyemingcheng: [
          ],
          lianxidianhua: [
          ],
          sfsh: [
          ],
          shhf: [
          ],
        },
      };
    },
    computed: {



    },
    created() {
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.shangjiariqi = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='chanpinmingcheng'){
              this.ruleForm.chanpinmingcheng = obj[o];
              this.ro.chanpinmingcheng = true;
              continue;
            }
            if(o=='xiangmufenlei'){
              this.ruleForm.xiangmufenlei = obj[o];
              this.ro.xiangmufenlei = true;
              continue;
            }
            if(o=='chanpintupian'){
              this.ruleForm.chanpintupian = obj[o].split(",")[0];
              this.ro.chanpintupian = true;
              continue;
            }
            if(o=='chanpinguige'){
              this.ruleForm.chanpinguige = obj[o];
              this.ro.chanpinguige = true;
              continue;
            }
            if(o=='rongzijine'){
              this.ruleForm.rongzijine = obj[o];
              this.ro.rongzijine = true;
              continue;
            }
            if(o=='suochoujine'){
              this.ruleForm.suochoujine = obj[o];
              this.ro.suochoujine = true;
              continue;
            }
            if(o=='shengchanchandi'){
              this.ruleForm.shengchanchandi = obj[o];
              this.ro.shengchanchandi = true;
              continue;
            }
            if(o=='shangjiariqi'){
              this.ruleForm.shangjiariqi = obj[o];
              this.ro.shangjiariqi = true;
              continue;
            }
            if(o=='chanpinxiangqing'){
              this.ruleForm.chanpinxiangqing = obj[o];
              this.ro.chanpinxiangqing = true;
              continue;
            }
            if(o=='qiyemingcheng'){
              this.ruleForm.qiyemingcheng = obj[o];
              this.ro.qiyemingcheng = true;
              continue;
            }
            if(o=='lianxidianhua'){
              this.ruleForm.lianxidianhua = obj[o];
              this.ro.lianxidianhua = true;
              continue;
            }
          }
        }
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.qiyemingcheng!=''&&json.qiyemingcheng) || json.qiyemingcheng==0){
                this.ruleForm.qiyemingcheng = json.qiyemingcheng
            }
            if((json.lianxidianhua!=''&&json.lianxidianhua) || json.lianxidianhua==0){
                this.ruleForm.lianxidianhua = json.lianxidianhua
            }
          }
        });
        this.$http.get('option/xiangmufenlei/xiangmufenlei', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.xiangmufenleiOptions = res.data.data;
          }
        });
      },

    // 多级联动参数
      // 多级联动参数
      info(id) {
        this.$http.get('chanpinxiangmu/detail/${id}', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

        //更新跨表属性
        var crossuserid;
        var crossrefid;
        var crossoptnum;
        this.$refs["ruleForm"].validate(valid => {
          if(valid) {
            if(this.type=='cross'){
                 var statusColumnName = localStorage.getItem('statusColumnName');
                 var statusColumnValue = localStorage.getItem('statusColumnValue');
                 if(statusColumnName && statusColumnName!='') {
                     var obj = JSON.parse(localStorage.getItem('crossObj'));
                     if(!statusColumnName.startsWith("[")) {
                         for (var o in obj){
                             if(o==statusColumnName){
                                 obj[o] = statusColumnValue;
                             }
                         }
                         var table = localStorage.getItem('crossTable');
                         this.$http.post(table+'/update', obj).then(res => {});
                     } else {
                            crossuserid=Number(localStorage.getItem('userid'));
                            crossrefid=obj['id'];
                            crossoptnum=localStorage.getItem('statusColumnName');
                            crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
                     }
                 }
            }
            if(crossrefid && crossuserid) {
                 this.ruleForm.crossuserid=crossuserid;
                 this.ruleForm.crossrefid=crossrefid;
                 var params = {
                     page: 1,
                     limit: 10,
                     crossuserid:crossuserid,
                     crossrefid:crossrefid,
                 }
                 this.$http.get('chanpinxiangmu/list', {
                  params: params
                 }).then(res => {
                     if(res.data.data.total>=crossoptnum) {
                         this.$message({
                          message: localStorage.getItem('tips'),
                          type: 'success',
                          duration: 1500,
                         });
                          return false;
                     } else {
                         // 跨表计算


                          this.$http.post('chanpinxiangmu/add', this.ruleForm).then(res => {
                              if (res.data.code == 0) {
                                  this.$message({
                                      message: '操作成功',
                                      type: 'success',
                                      duration: 1500,
                                      onClose: () => {
                                          this.$router.go(-1);
                                      }
                                  });
                              } else {
                                  this.$message({
                                      message: res.data.msg,
                                      type: 'error',
                                      duration: 1500
                                  });
                              }
                          });
                     }
                 });
             } else {


                  this.$http.post('chanpinxiangmu/add', this.ruleForm).then(res => {
                     if (res.data.code == 0) {
                          this.$message({
                              message: '操作成功',
                              type: 'success',
                              duration: 1500,
                              onClose: () => {
                                  this.$router.go(-1);
                              }
                          });
                      } else {
                          this.$message({
                              message: res.data.msg,
                              type: 'error',
                              duration: 1500
                          });
                      }
                  });
             }
          }
        });
      },
      // 获取uuid
      getUUID () {
        return new Date().getTime();
      },
      // 返回
      back() {
        this.$router.go(-1);
      },
      chanpintupianUploadChange(fileUrls) {
          this.ruleForm.chanpintupian = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

